func TestStringLiteralExpression(t *testing.T) {
	input := `"hello, world";`

	l := lexer.New(input)
	p := New(l)
	program := p.ParseProgram()
	checkParserErrors(t, p)

	stmt := program.Statements[0].(*ast.ExpressionStatement)
	literal, ok := stmt.Expression.(*ast.StringLiteral)
	if !ok {
		t.Fatalf("exp not *ast.StringLiteral. got=%T", stmt.Expression)
	}
	if literal.Value != "hello, world" {
		t.Fatalf("literal.Value not 'hello, world', got=%s", literal.Value)
	}

}

func TestLetStatements(t *testing.T) {
	tests := []struct {
		input              string
		expectedIdentifier string
		expectedValue      interface{}
	}{
		{"let x = 5;", "x", 5},
		{"let y = true;", "y", true},
		{"let foobar = y", "foobar", "y"},
	}

	for _, tt := range tests {
		l := lexer.New(tt.input)
		p := New(l)

		program := p.ParseProgram()
		checkParserErrors(t, p)

		if len(program.Statements) != 1 {
			t.Fatalf("program.Statements does not contain 1 statements. got=%d", len(program.Statements))
		}

		stmt := program.Statements[0]
		if !testLetStatement(t, stmt, tt.expectedIdentifier) {
			return
		}

		val := stmt.(*ast.LetStatement).Value
		if !testLiteralExpression(t, val, tt.expectedValue) {
			return
		}
	}
}

func testLetStatement(t *testing.T, s ast.Statement, name string) bool {
	if s.TokenLiteral() != "let" {
		t.Errorf("s.Tokenliteral not 'let'. got=%q", s.TokenLiteral())
		return false
	}
	if letStmt, ok := s.(*ast.LetStatement); !ok {
		t.Errorf("s not *ast.LetStatement. got=%T", s)
		return false
	} else if letStmt.Name.Value != name {
		t.Errorf("ltStmt.Name.Value not '%s'. got=%s", name, letStmt.Name.Value)
		return false
	} else if letStmt.Name.TokenLiteral() != name {
		t.Errorf("s.Name not %s. got=%s", name, letStmt.Name)
		return false
	}
	return true
}

